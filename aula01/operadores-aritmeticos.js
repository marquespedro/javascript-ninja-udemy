// operadores + , - , / , * 

//soma 
var sum = 1 + 3 ;
sum++;
sum += 1;
console.log('Soma' , sum);

//subtracao
var sub = 3 - 1; 
sub--;
sub -= 1;
console.log('Subtracao' , sub);

//multiplicacao
var mult = 3 * 3;
mult *= 2;
console.log('Multiplicacao' , mult);

//divisao
var div = 10/2;
div /= 2;
console.log('Divisao' , div);


